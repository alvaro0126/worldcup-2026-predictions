def _bar(rank: int, team: str, win: float, lead: bool, maxv: float) -> str:
    p = max(win / maxv, 0.012) if maxv else 0.012
    cls = "bar lead" if lead else "bar"
    fill = "fill lead" if lead else "fill"
    return (f'<div class="{cls}"><span class="rk num">{rank}</span>'
            f'<span class="tm">{_e(team)}<span class="cd">{_code(team)}</span></span>'
            f'<span class="track"><span class="{fill}" style="--p:{p:.4f}"></span></span>'
            f'<span class="pct">{win:.1f}%</span></div>')


def _odds_section(bm: BayesModel, teams: dict, agg: SimAgg) -> str:
    n = agg.n
    ranked = sorted(teams, key=lambda t: agg.champion[t], reverse=True)
    maxv = agg.champion[ranked[0]] / n * 100
    rows_top, rows_rest = [], []
    for i, t in enumerate(ranked, 1):
        win = agg.champion[t] / n * 100
        bar = _bar(i, t, win, i == 1, maxv)
        (rows_top if i <= 24 else rows_rest).append(bar)
    rest = ("".join(rows_rest))
    return f"""
<section id="odds"><div class="wrap reveal">
  <div class="sec-head"><span class="n">01</span><h2>Who wins it</h2></div>
  <p class="dek">Share of {n:,} simulated tournaments each team won. The leader is drawn in gold; everyone else in claret. A World Cup is built for upsets, so even the favourite clears only one tournament in seven.</p>
  <div class="odds">{''.join(rows_top)}<div id="oddsrest" class="hidden">{rest}</div></div>
  <div class="more-row"><button class="btn" id="moreodds" onclick="document.getElementById('oddsrest').classList.toggle('hidden');this.textContent=this.textContent.indexOf('All')>=0?'Show top 24':'All 48 teams'">All 48 teams</button></div>
</div></section>"""


